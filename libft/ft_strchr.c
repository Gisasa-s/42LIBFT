#include <string.h>
#include <stdio.h>

char	*ft_strchr(const char *s, int c)
{
	char*str;

	str = (char*)s;
	while (*str != '\0')
	{
		if (*str == c)
			return (str);
		str++;
	}
	if (*str == c)
		return (str);
	else
		return (NULL);
}
