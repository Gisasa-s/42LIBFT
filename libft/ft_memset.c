#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned int	i;
	char			*s;

	i = 0;
	s = (char*)b;
	while (i < len)
	{
		s[i] = c;
		i++;
	}
	return (s);
}
